    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        String OID = txtID.getText();
        String DriverID = loggedUser.id;
        String status = "Pending";
        Boolean found = false;
        ArrayList<String> tempArray = new ArrayList<>();
        if (!"".equals(OID)) {
            int confirmation = JOptionPane.YES_OPTION;
            if (JOptionPane.showConfirmDialog(rootPane, "Confirm assigning the order to you?", "Confirmation Assignment", confirmation) == JOptionPane.YES_OPTION) {
           try {
            try (FileReader file = new FileReader("Order.txt")) {
                Scanner reader = new Scanner(file);
                String line;
                String[] lineArr;
                
                while(reader.hasNext()){
                    line = reader.nextLine();
                    lineArr = line.split(";");
                    if(lineArr[0].equals(OID)) {
                        found = true;
                        tempArray.add (
                            lineArr[0] + ";" +
                            lineArr[1] + ";" +
                            lineArr[2] + ";" +
                            status + ";" +
                            lineArr[4] + ";" +
                            lineArr[5] + ";" +
                            lineArr[6] + ";" +        
                            lineArr[7] + ";" +
                            lineArr[8] + ";" +
                            DriverID
                        );
                    } else {
                        tempArray.add(line);
                    }
                }
                PrintWriter pr = new PrintWriter("Order.txt");
                for (String str : tempArray) {
                    pr.println(str);
                    
                }
                
                pr.close();

                
                
                file.close();
                } catch (Exception e) { 
                    JOptionPane.showMessageDialog(rootPane,"Error = " + e,"Error",JOptionPane.WARNING_MESSAGE);
            }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane,"Error = " + e,"Error",JOptionPane.WARNING_MESSAGE);
            }
        if(!found) {
            JOptionPane.showMessageDialog(rootPane,"Order Not Found!","Error",JOptionPane.WARNING_MESSAGE);
        }
        else {
        JOptionPane.showMessageDialog(rootPane,"Order Assignned!","Success",JOptionPane.INFORMATION_MESSAGE);
        txtID.setText("");
        txtDelivDate.setText("");
        txtRecDate.setText("");
        txtWeight.setText("");
        txtRecName.setText("");
        txtarRecAddr.setText("");
        txtSendName.setText("");
        txtarSendAddr.setText("");
        Ordertable(); 
                }
        
        
        }
        }
        else {
           JOptionPane.showMessageDialog(rootPane,"Please select an order in the table","Please choose an order",JOptionPane.WARNING_MESSAGE); 
        }
        
        
    }//GEN-LAST:event_btnAcceptActionPerformed

